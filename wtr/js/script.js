var map;

function initMap() {
  var loc = {lat: 40.735240, lng: -73.993934};
  console.log(loc);
  map = new google.maps.Map(document.getElementById('map'), {
    zoom: 16,
    center: loc,
    disableDefaultUI: true,
  });
  map.setOptions({draggable: true, zoomControl: true, scrollwheel: false, disableDoubleClickZoom: true});
}

function mapMove(lat, lng){
  console.log('animate to lat:' + lat + ' lng:' + lng);
  
  var latLng = new google.maps.LatLng(lat, lng);
  window.setTimeout(function(){
    map.panTo(latLng);
  },300); // this number is the delay after you click. 
}